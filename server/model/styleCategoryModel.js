import mongoose from "mongoose";

const styleCategorySchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    title: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const StyleCategory = mongoose.model("StyleCategory", styleCategorySchema);

export default StyleCategory;
